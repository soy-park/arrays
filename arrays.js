var drinks = ["tea", "coffee", "water", "milk"];
var pets = [0, 1, 2, 3];
var coldDrink = [true, false, false, true];

//method pop will remove last element
drinks.pop();
console.log(drinks);

//method push will add element to end of array
pets.push(4);
console.log(pets);

//method shift will remove first element
coldDrink.shift();
console.log(coldDrink);

//Index begins with 0 at start of array and increases by 1.
//For example, the first element in drinks is tea, and is at index 0. 
//The third index of pets is 3. 

